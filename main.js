palavraTexto = document.querySelectorAll(".forte")
// horror - caixa e audio

// caixa
abrir1 = document.getElementById("palavra1")
fechar1 = document.getElementById("fechar1")
caixa1 = document.getElementById("horror")

abrir1.addEventListener("click", (e) => {
    caixa1.style.visibility = "visible"
    palavraTexto.style.visibility = "visible"
})

fechar1.addEventListener("click", (e) => {
    caixa1.style.visibility = "hidden"
})


// audio 

audio1 = document.getElementById("audio1")
imagem1 = document.getElementById("imagem1")

imagem1.addEventListener('mouseover', function () {
    audio1.play();
})

imagem1.addEventListener('mouseleave', function () {
    audio1.pause();
    audio1.currentTime = 0;
})


